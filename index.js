module.exports = {

    /**
     * Suma de dos numeros
     * @example
     * n1 = 1, n2 = 2 , resultado = 3
     * @param {*} n1 numero uno de la suma
     * @param {*} n2 numero dos de la suma
     * @returns  suma de dos numeros
     */

    suma: function(n1,n2){
        return n1+n2;
    },

      /**
       * resta de dos numeros
     * @example
     * n1 = 1, n2 = 2 , resultado = -1
     * @param {*} n1 numero uno de la resta
     * @param {*} n2 numero dos de la resta
     * @returns  resta de dos numeros
     */

    resta: function(n1,n2){
        return n1-n2;
    },

      /**
     * multiplicacion de dos numeros
     * @example
     * n1 = 1, n2 = 2 , resultado = 2
     * @param {*} n1 numero uno de la multiplicacion
     * @param {*} n2 numero dos de la multiplicacion
     * @returns  multiplicacion de dos numeros
     */

    multiplicar: function(n1,n2){
        return n1*n2;
    },

      /**
       * division de dos numeros
     * @example
     * n1 = 1, n2 = 2 , resultado = 0.5
     * @param {*} n1 numero uno de la division 
     * @param {*} n2 numero dos de la division 
     * @returns   division de dos numeros
     */

    dividir: function(n1,n2){
        return n1/n2;
    }



}